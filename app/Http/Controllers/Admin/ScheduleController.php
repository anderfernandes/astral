    {
      $shifts = Shift::where('start', '>=', now()->startOfDay()->toDateTimeString())
                     ->orderBy('start', 'asc')
                     ->get();

        return view('admin.schedules.show')->withSchedule($schedule)->withShifts($shifts);
    }

